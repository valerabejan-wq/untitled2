package com;

public class ShiftExe {
    public static void main(String[] args) {
        // <<  >> четные не чётные '4' << 4 2 1 ->[ 0.0.1 -> 1.0.0 ] inversia

        System.out.println(0b100);
// 1.0.0 << 2 -> 1.0.0.(0.0)
        //1e,0d,0c,0b,0a -> (1x2'd)+(0x2'c)+(0x2'b)+(0x2'a) = 16 a=0;b=1;c=2;d=3;e=4.

        System.out.println(2L << 31); // umnojiti

        System.out.println(4 >> 2); // razdeliti

        System.out.println(0b001);


    }
}
